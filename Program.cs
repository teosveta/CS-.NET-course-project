using System;
using System.Windows.Forms;

namespace LibraryProject
{
    /// <summary>
    /// Статичен клас Program - съдържа входната точка на приложението.
    /// 
    /// Класът е static, защото:
    /// - Не се нуждаем от създаване на обект от този клас
    /// - Main() методът трябва да бъде достъпен директно при стартиране
    /// </summary>
    static class Program
    {
        /// <summary>
        /// Главна входна точка на приложението.
        /// 
        /// Атрибутът [STAThread] е задължителен за Windows Forms приложения.
        /// STA означава "Single-Threaded Apartment" и е необходим за
        /// правилна работа с COM компоненти и Windows Forms контроли.
        /// </summary>
        [STAThread]
        static void Main()
        {
            /*
             * EnableVisualStyles() активира визуалните стилове на Windows.
             * Това прави контролите да изглеждат по-модерно,
             * съобразено с текущата тема на Windows.
             */
            Application.EnableVisualStyles();

            /*
             * SetCompatibleTextRenderingDefault(false) указва, че новите
             * контроли трябва да използват GDI+ за рендиране на текст
             * вместо остарелия GDI. Това осигурява по-добро качество на текста.
             */
            Application.SetCompatibleTextRenderingDefault(false);

            /*
             * Application.Run() стартира приложението и показва посочената форма.
             * 
             * Какво прави Application.Run():
             * 1. Създава нов обект MainForm (извиква конструктора)
             * 2. Показва формата на екрана
             * 3. Стартира "message loop" - безкраен цикъл, който:
             *    - Получава съобщения от Windows (клик на мишка, натискане на клавиш)
             *    - Препраща ги към съответните event handlers
             *    - Продължава докато формата не бъде затворена
             * 
             * Когато MainForm се затвори, Application.Run() приключва
             * и приложението се прекратява.
             */
            Application.Run(new MainForm());
        }
    }
}
