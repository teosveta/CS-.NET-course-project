using System;

namespace LibraryProject
{
    /// <summary>
    /// Интерфейс IBorrowable - дефинира поведението на обекти, които могат да се заемат.
    /// 
    /// Този интерфейс е пример за ООП концепцията "Интерфейси".
    /// Всеки клас, който имплементира този интерфейс, се задължава да предостави
    /// реализация на методите Borrow() и Return(), както и свойствата IsBorrowed и BorrowerName.
    /// </summary>
    public interface IBorrowable
    {
        /*
         * ========================================
         * СВОЙСТВА (Properties)
         * ========================================
         * Свойствата в интерфейса дефинират какви данни трябва да поддържа
         * всеки клас, който имплементира интерфейса.
         */

        /// <summary>
        /// Свойство, което показва дали обектът е зает в момента.
        /// get - позволява четене на стойността
        /// set - позволява задаване на стойността
        /// </summary>
        bool IsBorrowed { get; set; }

        /// <summary>
        /// Свойство, което съхранява името на човека, заел обекта.
        /// Ако обектът не е зает, стойността е празен низ.
        /// </summary>
        string BorrowerName { get; set; }

        /*
         * ========================================
         * МЕТОДИ (Methods)
         * ========================================
         * Методите в интерфейса дефинират какви действия трябва да поддържа
         * всеки клас, който имплементира интерфейса.
         * Забележка: Методите в интерфейса нямат тяло (имплементация).
         */

        /// <summary>
        /// Метод за заемане на обекта.
        /// Класовете, които имплементират интерфейса, трябва да предоставят
        /// конкретна логика за това как се извършва заемането.
        /// </summary>
        /// <param name="borrowerName">Името на човека, който заема обекта</param>
        void Borrow(string borrowerName);

        /// <summary>
        /// Метод за връщане на заетия обект.
        /// Класовете, които имплементират интерфейса, трябва да предоставят
        /// конкретна логика за това как се извършва връщането.
        /// </summary>
        void Return();
    }
}
